    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        if (validaNumeros(exp.getText()) == true) {
            String expressao = exp.getText();
            if (comVetor.isSelected()) {
                calculadora.constroiVetor(expressao.length());
            } else {
                calculadora.constroiLista();
            }
            calculadora.Calcular(expressao);
            txtResultado.setText(calculadora.getResultadoFinal());
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

