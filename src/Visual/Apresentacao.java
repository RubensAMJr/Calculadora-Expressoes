    private boolean validaNumeros(String text) {
        if (Character.isDigit(text.charAt(text.length() - 1)) || !Character.isDigit(text.charAt(0)) || !text.contains(" ")) {
            System.out.println(Character.isDigit(text.charAt(text.length() - 1)));
            System.out.println(Character.isDigit(text.charAt(0)));
            System.out.println(text.contains(" "));
            JOptionPane.showMessageDialog(this, "Expressão Invalida!");
            return false;
        } else {
            return true;
        }
    }
